public class Loops {
    public static void main(String args[]) {

        // for loop

        System.out.println("Using for loop print 10 num");
       for (int i=1;i<=10;i++){
        System.out.print(i+" ");
       }
       System.out.println( );


    //    while loop (Entry Control loop)
    // initialize value , gives condition in while loop ,
    //  in the loop keep increment 


    System.out.println( );  


    System.out.println("Using while loop");
    int i=1;
    while(i<11){
      System.out.println(i);
      i=i+1;
    }


    // Do while loop (Exit control loop)
    System.out.println("Using Do while loop ");
    int n = 1;
        do{
            
            System.out.println(n);
            n=n+1;
        }while(n<11);



   



    }
}
